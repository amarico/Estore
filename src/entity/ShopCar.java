package entity;

public class ShopCar {
	
	//用于存储用户的购物车信息
	
	private int sid;
	private int uid;
	private int gid;
	private int gnumber;
	
	public ShopCar(){
		
	}
	
	
	public ShopCar(int sid, int uid, int gid, int gnumber) {
		//super();
		this.sid = sid;
		this.uid = uid;
		this.gid = gid;
		this.gnumber = gnumber;
	}


	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public int getGid() {
		return gid;
	}
	public void setGid(int gid) {
		this.gid = gid;
	}

	public void setGnumber(int gnumber) {
		this.gnumber = gnumber;
	}

	public int getGnumber() {
		return gnumber;
	}
	
	
	

}
