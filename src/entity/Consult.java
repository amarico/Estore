package entity;

import java.util.Date;

//×ÉÑ¯±í
public class Consult {

	private int cid;
	private int uid;
	private int gid;
	private String text;
	private String retext;
	private Date date;
	
	public Consult(){
		
	}
	
	public Consult(int cid, int uid, int gid, String text, String retext,
			Date date) {
		//super();
		this.cid = cid;
		this.uid = uid;
		this.gid = gid;
		this.text = text;
		this.retext = retext;
		this.date = date;
	}
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public int getGid() {
		return gid;
	}
	public void setGid(int gid) {
		this.gid = gid;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getRetext() {
		return retext;
	}
	public void setRetext(String retext) {
		this.retext = retext;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	
	
	
}
