package entity;
//商品分类表
public class Classify {
	
	private int cid;
	private String cname;
	private int  cparent;
	
	
	public Classify(){
		
	}
	
	public Classify(int cid, String cname, int cparent) {
		//super();
		this.cid = cid;
		this.cname = cname;
		this.cparent = cparent;
	}
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public int getCparent() {
		return cparent;
	}
	public void setCparent(int cparent) {
		this.cparent = cparent;
	}
	
	
	
	
	
	

}
